package models;

public class PhongGiaoDich {
	String mapgd;
	String tenpgd;
	public PhongGiaoDich(String mapgd, String tenpgd) {
		super();
		this.mapgd = mapgd;
		this.tenpgd = tenpgd;
	}
	public String getMapgd() {
		return mapgd;
	}
	public void setMapgd(String mapgd) {
		this.mapgd = mapgd;
	}
	public String getTenpgd() {
		return tenpgd;
	}
	public void setTenpgd(String tenpgd) {
		this.tenpgd = tenpgd;
	}
	
	
}
